#!/bin/bash

if [[ $1 == "" ]];then
	echo -e "
\033[0;31mUsage\033[1;37m : nhost.sh \033[0;31m[-d]
    -d\033[1;37m detailed output

	nhost.sh \033[0;31m'options' 'url'

		nhost.sh google.com
	 	nhost -d google.com\033[1;37m
	";exit
fi

if [[ $1 == "-d" ]];then
	echo -e "
------------------------------------------ \033[0;31mSOA\033[1;37m ------------------------------------------
"
	host -t SOA $2
	echo -e "
------------------------------------------ \033[0;31mA\033[1;37m ------------------------------------------
"
	host -t A $2
	echo -e "
------------------------------------------ \033[0;31mAAAA\033[1;37m ------------------------------------------
"
	host -t AAAA $2
	echo -e "
------------------------------------------ \033[0;31mNS\033[1;37m ------------------------------------------
"
	host -t NS $2
	echo -e "
------------------------------------------ \033[0;31mCNAME\033[1;37m ------------------------------------------
"
	host -t CNAME $2
	echo -e "
------------------------------------------ \033[0;31mMX\033[1;37m ------------------------------------------
"
	host -t MX $2
	echo -e "
------------------------------------------ \033[0;31mHINFO\033[1;37m ------------------------------------------
"
	host -t HINFO $2
	echo -e "
------------------------------------------ \033[0;31mTXT\033[1;37m ------------------------------------------
"
	host -t TXT $2
	echo -e "
--------------------------------------------------------------------------------------------
"

else
	printf '\n'
	host -t SOA $1
	host -t A $1
	host -t AAAA $1
	host -t NS $1
	host -t CNAME $1
	host -t MX $1
	host -t HINFO $1
	host -t TXT $1
	printf '\n'
fi
